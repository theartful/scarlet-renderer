pub struct HaltonSampler {
    current_dimension: u32,
    current_sample: u32,
    index: u64,
    permutations: Vec<Vec<u32>>,
}

impl HaltonSampler {
    pub fn init() -> Self {
        Self {
            current_dimension: 0,
            current_sample: 0,
            index: 0,
            permutations: Vec::new(),
        }
    }
    fn allocate_permutation_if_necessary(&mut self, count: usize) {
        for idx in self.permutations.len()..self.current_dimension as usize + count - 1 {
            let mut perm = (0..PRIME_TABLE[idx + 1]).collect::<Vec<u32>>();
            perm[..].shuffle(&mut rand::thread_rng());
            self.permutations.push(perm);
        }
    }
    fn get_permutation(&mut self, index: u32) -> &[u32] {
        &self.permutations[index as usize - 1][..]
    }
}

impl Sampler for HaltonSampler {
    fn get_1d(&mut self) -> Float {
        self.allocate_permutation_if_necessary(1);
        let res = if self.current_dimension == 0 {
            fast_radical_inverse(self.current_dimension, self.index)
        } else {
            fast_scrambled_radical_inverse(
                self.current_dimension,
                self.index,
                self.get_permutation(self.current_dimension),
            )
        };
        self.current_dimension += 1;
        res
    }
    fn get_2d(&mut self) -> (Float, Float) {
        self.allocate_permutation_if_necessary(2);
        let res = (
            if self.current_dimension == 0 {
                fast_radical_inverse(self.current_dimension, self.index)
            } else {
                fast_scrambled_radical_inverse(
                    self.current_dimension,
                    self.index,
                    self.get_permutation(self.current_dimension),
                )
            },
            fast_scrambled_radical_inverse(
                self.current_dimension + 1,
                self.index,
                self.get_permutation(self.current_dimension + 1),
            ),
        );
        self.current_dimension += 2;
        res
    }
    fn advance_sample(&mut self) {
        self.current_sample += 1;
        self.current_dimension = 0;
        self.index += 1;
    }
    fn advance_pixel(&mut self) {
        self.current_sample = 0;
        self.current_dimension = 0;
        self.index += 1;
    }
}

pub fn radical_inverse(base: u64, value: u64) -> Float {
    let mut x = value;
    let mut res: u64 = 0;
    let mut num_digits = 0;
    while x > 0 {
        let digit = x % base;
        x /= base;
        res = res * base + digit;
        num_digits += 1;
    }
    res as Float / (base as Float).powi(num_digits)
}

fn flip_bits(value: u64) -> u64 {
    let value_1 = ((value & 0xffffffff00000000) >> 32) | ((value & 0x00000000ffffffff) << 32);
    let value_2 = ((value_1 & 0xffff0000ffff0000) >> 16) | ((value_1 & 0x0000ffff0000ffff) << 16);
    let value_3 = ((value_2 & 0xff00ff00ff00ff00) >> 8) | ((value_2 & 0x00ff00ff00ff00ff) << 8);
    let value_4 = ((value_3 & 0xf0f0f0f0f0f0f0f0) >> 4) | ((value_3 & 0x0f0f0f0f0f0f0f0f) << 4);
    let value_5 = ((value_4 & 0xcccccccccccccccc) >> 2) | ((value_4 & 0x3333333333333333) << 2);
    ((value_5 & 0xaaaaaaaaaaaaaaaa) >> 1) | ((value_5 & 0x5555555555555555) << 1)
}

pub fn radical_inverse_base2(value: u64) -> Float {
    flip_bits(value) as Float / (2. as Float).powi(64)
}

fn generic_radical_inverse<const B: u64>(value: u64) -> Float {
    let mut x = value;
    let mut res: u64 = 0;
    let mut num_digits = 0;
    while x > 0 {
        let digit = x % B;
        x /= B;
        res = res * B + digit;
        num_digits += 1;
    }
    res as Float / (B as Float).powi(num_digits)
}

fn generic_scrambled_radical_inverse<const B: u64>(value: u64, permutation: &[u32]) -> Float {
    let mut x = value;
    let mut res: u64 = 0;
    let mut num_digits = 0;
    while x > 0 {
        let digit = x % B;
        x /= B;
        if cfg!(debug_assertions) {
            res = res * B + permutation[digit as usize] as u64;
        } else {
            res = res * B + unsafe { *permutation.get_unchecked(digit as usize) as u64 };
        }
        num_digits += 1;
    }
    res as Float / (B as Float).powi(num_digits)
}

